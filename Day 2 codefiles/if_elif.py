# Grade calculator using if-elif-else
score = 85

if score >= 90:
    print("Grade: A")
    print("Excellent work!")
elif score >= 80:
    print("Grade: B")
    print("Great job!")
elif score >= 70:
    print("Grade: C")
    print("Good effort!")
elif score >= 60:
    print("Grade: D")
    print("You passed, but study more.")
else:
    print("Grade: F")
    print("You need to retake this.")

