# Comparison operators return True or False
x = 10
y = 20

print(x == y)   # Equal to: False (10 is not equal to 20)
print(x != y)   # Not equal to: True (10 is not equal to 20)
print(x > y)    # Greater than: False (10 is not greater than 20)
print(x < y)    # Less than: True (10 is less than 20)
print(x >= 10)  # Greater than or equal: True (10 is equal to 10)
print(x <= 5)   # Less than or equal: False (10 is not less than or equal to 5)


