# Arithmetic Operators - Basic Math Operations

a = 10
b = 3

print(a + b)   # Addition: 10 + 3 = 13
print(a - b)   # Subtraction: 10 - 3 = 7
print(a * b)   # Multiplication: 10 * 3 = 30
print(a / b)   # Division: 10 / 3 = 3.333 (with decimals)
print(a % b)   # Modulus: 10 % 3 = 1 (remainder when 10 is divided by 3)
print(a ** b)  # Exponent: 10^3 = 1000 (10 raised to the power of 3)
print(a // b)  # Floor Division: 10 // 3 = 3 (only the whole number part)