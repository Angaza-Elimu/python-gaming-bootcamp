# Variables are containers for storing data
# Let's say you have a box to store things
# You can give the box a name, like "my_box"

my_box = "toys"
print(my_box)  # Output: toys

# We can store messages in variables
my_message = "Hello World!"
print(my_message)  # Output: Hello World!

# We can store numbers in variables
width = 10
length = 20
print(width, length)  # Output: 10 20

# Variables for a game character
player_name = "DragonSlayer"
health = 100
level = 5
is_alive = True

print("Player:", player_name)
print("Health:", health)
print("Level:", level)
print("Alive?", is_alive)